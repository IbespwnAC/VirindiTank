namespace uTank2
{
    using System;

    internal class MyQuad<A, B, C, D>
    {
        public A a;
        public B b;
        public C c;
        public D d;

        public MyQuad()
        {
        }

        public MyQuad(A A_0, B A_1, C A_2, D A_3)
        {
            this.a = A_0;
            this.b = A_1;
            this.c = A_2;
            this.d = A_3;
        }
    }
}

